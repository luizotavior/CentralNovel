	<h2>A Aliança Novel</h2>
	<p style="text-align: justify;">Há muitos anos o Brasil vem testemunhando o nascimento de diversos grupos e indivíduos que dedicam o seu tempo para traduzir aquilo que gostam, e assim, muitas webnovels e lightnovels puderam ser apreciadas com o nosso bom português-brasileiro.</p>
	<p>Dantes confinadas ao seu idioma original, ou quiçá traduzidas para o inglês, hoje estas histórias estão sendo traduzidas e revisadas por pessoas apaixonadas por aquilo que fazem. Com este movimento de tradução, também testemunhamos o nascimento de novels brasileiras, provando a força que as web e lightnovels alcançou nos últimos tempos.</p>
	<p>Por conseguinte, os grupos de tradução sentiram a necessidade de ter um canal de comunicação, um local onde pudessem ajudar e ser ajudados.

</p><p>E assim, nasceu a Aliança Novel Brasil.</p>
<p>A Aliança não dita regras, não estabelece diretrizes, tão pouco lhe fora outorgado o poder para interferir nos projetos ou modus operandi dos grupos de tradução.</p>
<p>A proposta da Aliança é ser um canal direto com o representante de cada grupo de tradução, visando aproximar as equipes, seja na divulgação de eventos, projetos ou mesmo em dúvidas técnicas ou operacionais.</p>
<p>Você representa um grupo ou possui um projeto de tradução, mas ainda não participa da Aliança? Saiba que a Aliança está de portas abertas e todos os grupos são bem-vindos.</p>
<p>Para fazer parte da Aliança o Grupo deve ter pelo menos 3 Meses de Existencia e Inserir em seu Site o Simbolo da Aliança com o Lik direcionando para <a href="https://centralnovel.com.br/alianca">https://centralnovel.com.br/alianca</a>
</p>


