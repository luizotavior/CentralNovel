<h4>Por que a Central Novel resolveu incluir anuncios em seu site?</h4>
<p style="text-align: justify;">
A Central Novel decidiu que devido ao alto custo de manter o site online, assim iremos exibir 2 anuncios, o dinheiro arecadado com os anuncios serão utilizados para pagar a Hospedagem, caso conseguirmos mais dinheiro que o necessario para mantar o site, o dinheiro que sobrar será DOADO para algum grupo parceiro da nossa escolha.
</p>
